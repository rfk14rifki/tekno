    private void login(){
    String username = txt_username.getText();
    String password = txt_password.getText();
        try {
            String SQL = "select iduser,nama,username,hakakses,status from user where username = '"+username+"' "
                    + "and password =md5('"+password+"')";
            ResultSet rs = koneksi.ExecuteQuery(SQL);
            if(rs.next()){
                if(rs.getString(5).equals("1")){
                    pb.setVisible(true);
                    pb.setStringPainted(true);
                    pb.setFont(new Font("sans serif",Font.BOLD,16));
                    loading = -1;
                    time = new Timer(FOUR_SECONDS, new TimerListener());
                    time.start();
                    sesi.setIdPetugas(rs.getString(1));
                    sesi.setNama(rs.getString(2));
                    sesi.setUsername(rs.getString(3));
                    sesi.setLevel(rs.getString(4));
//                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Akun anda di NonAktifkan\nSilahkan Hubungi Administrator\nUntuk MengAktifkan Kembali","Warning",JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Silahkan periksa kembali\nUsername & Password\nYang anda masukan.","SIGN IN FAILED",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
        }
    }

